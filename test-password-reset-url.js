/**
 * Test script to verify password reset URL configuration
 * This script tests the password reset functionality and logs the URL being used
 */

const { createClientComponentClient } = require('@supabase/auth-helpers-nextjs');

async function testPasswordResetURL() {
  console.log('🔍 Testing Password Reset URL Configuration...\n');
  
  // Test environment variables
  console.log('Environment Variables:');
  console.log('NEXT_PUBLIC_SITE_URL:', process.env.NEXT_PUBLIC_SITE_URL);
  console.log('NEXT_PUBLIC_SUPABASE_URL:', process.env.NEXT_PUBLIC_SUPABASE_URL);
  console.log('NEXT_PUBLIC_SUPABASE_ANON_KEY:', process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY ? '[SET]' : '[NOT SET]');
  console.log('');
  
  // Check the hardcoded URL that should be used in production
  const productionUrl = 'https://skillswapappmvp.vercel.app';
  console.log('Hardcoded production URL:', productionUrl);
  console.log('Expected reset URL:', `${productionUrl}/auth/reset-password`);
  console.log('');
  
  // Test with a dummy email to see what happens (this will fail but show us the configuration)
  try {
    const supabase = createClientComponentClient();
    console.log('Testing password reset with dummy email...');
    
    const result = await supabase.auth.resetPasswordForEmail('test@example.com', {
      redirectTo: `${productionUrl}/auth/reset-password`,
    });
    
    console.log('Reset password result:', result);
    
    if (result.error) {
      console.log('Expected error (using dummy email):', result.error.message);
    } else {
      console.log('✅ Password reset function executed without client-side errors');
    }
    
  } catch (error) {
    console.error('❌ Error testing password reset:', error.message);
  }
  
  console.log('\n📋 Summary:');
  console.log('- The client-side code should use the hardcoded production URL');
  console.log('- Emails are generated by Supabase backend, not our frontend');
  console.log('- The redirectTo parameter should control the email link destination');
  console.log('\n🔧 Next Steps:');
  console.log('1. Test with a real email address');
  console.log('2. Check Supabase dashboard for email template configuration');
  console.log('3. Verify that the latest deployment is live');
}

// Run the test
testPasswordResetURL().catch(console.error);
